    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $rating=null;
        if($request['type']=="ITEMRATING"){
            $rating=MenuItems::find($request['menuItemId'])->rating()->create(['userId'=>Auth::user()->id,'value'=>$request['value']]);
            return $rating;
        }else if($request['type']=="SPRATING"){
            $rating=CHRLServiceProviders::find($request['serviceProviderId'])->rating()->create(['userId'=>Auth::user()->id,'value'=>$request['value']]);
        }
        return $rating;
    }
